    public void tampil_database() {
    Object[] kolom = {"ID", "Mata Kuliah", "Deskripsi", "Deadline"};
    tabmode = new DefaultTableModel(null, kolom);
    tabel_tugas.setModel(tabmode);
    tabel_tugas.setAutoCreateRowSorter(true);


    try {
        hasil = aa.tampilData();
        while (hasil.next()) {
            aa.setID(hasil.getString("id_tugas"));
            aa.setMatkul(hasil.getString("mata_kuliah"));
            aa.setDeskripsi(hasil.getString("deskripsi"));
            aa.setDeadline(hasil.getString("deadline"));

            String[] data = {aa.getID(), aa.getMatkul(), aa.getDeskripsi(), aa.getDeadline()};
            tabmode.addRow(data);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
    }
}
    public void tampil_statistik() {
    try {
        Connection con = KoneksiMysql.koneksiDB();
        Statement st = con.createStatement();

        // Jumlah tugas
        ResultSet rs1 = st.executeQuery("SELECT COUNT(*) FROM tugas");
        if (rs1.next()) {
            lbl_jumlah_tugas.setText("Jumlah Tugas: " + rs1.getInt(1));
        }

        // Jumlah matkul unik
        ResultSet rs2 = st.executeQuery("SELECT COUNT(DISTINCT mata_kuliah) FROM tugas");
        if (rs2.next()) {
            lbl_jumlah_matkul.setText("Jumlah Matkul: " + rs2.getInt(1));
        }

        // Deadline terdekat
        ResultSet rs3 = st.executeQuery("SELECT deadline FROM tugas WHERE deadline >= CURDATE() ORDER BY deadline ASC LIMIT 1");
        if (rs3.next()) {
            lbl_deadline_terdekat.setText("Deadline Terdekat: " + rs3.getString(1));
        } else {
            lbl_deadline_terdekat.setText("Deadline Terdekat: -");
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}

