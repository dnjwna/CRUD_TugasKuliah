   private boolean validasiInput() {
    String id = txt_id.getText().trim();
    String matkul = txt_matkul.getText().trim();
    String deskripsi = txt_deskripsi.getText().trim();
    Date deadline = date_deadline.getDate();

    if (id.isEmpty() || matkul.isEmpty() || deskripsi.isEmpty() || deadline == null) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return false;
    }

    if (!id.matches("[a-zA-Z0-9]+") || id.length() > 10) {
        JOptionPane.showMessageDialog(this, "ID hanya boleh huruf/angka (maks. 10 karakter)");
        return false;
    }

    if (matkul.length() > 50) {
        JOptionPane.showMessageDialog(this, "Nama mata kuliah terlalu panjang (maks. 50 karakter)");
        return false;
    }

    return true;
}

