                String deadline = new SimpleDateFormat("yyyy-MM-dd").format(date_deadline.getDate());
                aa.setID(txt_id.getText());
                aa.setMatkul(txt_matkul.getText());
                aa.setDeskripsi(txt_deskripsi.getText());
