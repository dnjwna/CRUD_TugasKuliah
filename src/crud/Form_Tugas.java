    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        String keyword = txt_cari.getText().trim();
    if (keyword.isEmpty()) {
        tampil_database(); // tampilkan semua
        return;
    }

    Object[] kolom = {"ID", "Mata Kuliah", "Deskripsi", "Deadline"};
    tabmode = new DefaultTableModel(null, kolom);
    tabel_tugas.setModel(tabmode);

    try {
        String sql = "SELECT * FROM tugas WHERE mata_kuliah LIKE '%" + keyword + "%'";
        Connection con = KoneksiMysql.koneksiDB();
        Statement st = con.createStatement();
        ResultSet hasil = st.executeQuery(sql);

        while (hasil.next()) {
            String[] data = {
                hasil.getString("id_tugas"),
                hasil.getString("mata_kuliah"),
                hasil.getString("deskripsi"),
                hasil.getString("deadline")
            };
            tabmode.addRow(data);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal mencari data: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_cariActionPerformed

